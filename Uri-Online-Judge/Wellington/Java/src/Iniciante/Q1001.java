package Iniciante;

import java.util.Scanner;

public class Q1001 {
    
    public static void main(String[]args){
        //Criação do objeto in para ler do terminal
        Scanner in = new Scanner(System.in);
        //declaração das variaveis
        int a, b, x;
        //ler duas variaveis
        a = in.nextInt();
        b = in.nextInt();
        //soma duas variaveis
        x = a + b;
        //imprimi o resultado
        System.out.println("X = " + x);
        
    }
    
}
